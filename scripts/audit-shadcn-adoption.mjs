#!/usr/bin/env node
import { mkdirSync, readdirSync, readFileSync, statSync, writeFileSync } from "node:fs";
import { dirname, join, relative } from "node:path";

const ROOT = process.cwd();
const SRC = join(ROOT, "packages/ui/src");
const OUT = join(ROOT, "docs/shadcn-adoption-report.md");
const CONTROL_RE = /<(button|input|select|table|textarea)\b/g;
const ALLOWED_PATH_PARTS = ["packages/ui/src/components/ui/", "packages/ui/src/components/ui\\"];

function walk(dir, files = []) {
  for (const entry of readdirSync(dir)) {
    if (["node_modules", ".next", "dist", "coverage", "playwright-report"].includes(entry))
      continue;
    const path = join(dir, entry);
    const st = statSync(path);
    if (st.isDirectory()) walk(path, files);
    else if (/\.tsx$/.test(path)) files.push(path);
  }
  return files;
}

function isAllowed(path) {
  const rel = relative(ROOT, path).replaceAll("\\", "/");
  return ALLOWED_PATH_PARTS.some((part) => rel.includes(part.replaceAll("\\", "/")));
}

const findings = [];
for (const file of walk(SRC)) {
  if (isAllowed(file)) continue;
  const rel = relative(ROOT, file).replaceAll("\\", "/");
  const source = readFileSync(file, "utf8");
  const lines = source.split(/\r?\n/);
  for (const [index, line] of lines.entries()) {
    for (const match of line.matchAll(CONTROL_RE)) {
      findings.push({ file: rel, line: index + 1, control: match[1] });
    }
  }
}

const byControl = new Map();
const byFile = new Map();
for (const finding of findings) {
  byControl.set(finding.control, (byControl.get(finding.control) ?? 0) + 1);
  byFile.set(finding.file, (byFile.get(finding.file) ?? 0) + 1);
}

const topFiles = [...byFile.entries()]
  .sort((a, b) => b[1] - a[1] || a[0].localeCompare(b[0]))
  .slice(0, 25);
const details = findings
  .sort(
    (a, b) => a.file.localeCompare(b.file) || a.line - b.line || a.control.localeCompare(b.control)
  )
  .map((f) => `| ${f.file} | ${f.line} | \`<${f.control}>\` |`)
  .join("\n");

const markdown = `# shadcn/ui Adoption Report

Generated by \`node scripts/audit-shadcn-adoption.mjs\`.

## Summary

| Metric | Count |
|---|---:|
| Raw interactive/table controls outside \`components/ui\` | ${findings.length} |
| Files with raw controls | ${byFile.size} |
| Raw buttons | ${byControl.get("button") ?? 0} |
| Raw inputs | ${byControl.get("input") ?? 0} |
| Raw selects | ${byControl.get("select") ?? 0} |
| Raw tables | ${byControl.get("table") ?? 0} |
| Raw textareas | ${byControl.get("textarea") ?? 0} |

## Top migration targets

| File | Count |
|---|---:|
${topFiles.map(([file, count]) => `| ${file} | ${count} |`).join("\n") || "| _None_ | 0 |"}

## Findings

| File | Line | Control |
|---|---:|---|
${details || "| _None_ |  |  |"}
`;

mkdirSync(dirname(OUT), { recursive: true });
writeFileSync(OUT, markdown);
console.log(
  `Wrote ${relative(ROOT, OUT)} (${findings.length} raw controls across ${byFile.size} files).`
);
